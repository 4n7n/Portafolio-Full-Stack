// Funci贸n para cargar icono SVG como elemento
export async function loadSVGIcon(path, size = 24, className = '') {
  try {
    const response = await fetch(path);
    const svgText = await response.text();
    
    const div = document.createElement('div');
    div.className = `icon ${className}`;
    div.style.width = `${size}px`;
    div.style.height = `${size}px`;
    div.innerHTML = svgText;
    
    return div;
  } catch (error) {
    console.warn(`Error cargando SVG: ${path}`, error);
    return null;
  }
}

// Funci贸n para obtener icono por ruta
export function getIcon(category, name) {
  return ICONS_DATA[category]?.[name] || null;
}

// Funci贸n para obtener todos los iconos de una categor铆a
export function getCategoryIcons(category) {
  return ICONS_DATA[category] || {};
}

// Funci贸n para crear elemento de icono usando SVG real
export async function createIconElement(category, name, size = 24, className = '') {
  const iconPath = getIcon(category, name);
  if (!iconPath) return null;
  
  if (typeof iconPath === 'string') {
    return await loadSVGIcon(iconPath, size, className);
  } else if (iconPath.icon) {
    return await loadSVGIcon(iconPath.icon, size, className);
  }
  
  return null;
}

// Funci贸n para crear icono con fallback
export function createIconWithFallback(category, name, size = 24, fallbackEmoji = '') {
  const container = document.createElement('div');
  container.className = 'icon-container';
  container.style.width = `${size}px`;
  container.style.height = `${size}px`;
  container.style.display = 'flex';
  container.style.alignItems = 'center';
  container.style.justifyContent = 'center';

  // Intentar cargar SVG
  createIconElement(category, name, size).then(svgElement => {
    if (svgElement) {
      container.innerHTML = '';
      container.appendChild(svgElement);
    } else {
      // Fallback a emoji
      container.innerHTML = `<span style="font-size: ${size * 0.8}px">${fallbackEmoji}</span>`;
    }
  }).catch(() => {
    // Fallback a emoji si falla
    container.innerHTML = `<span style="font-size: ${size * 0.8}px">${fallbackEmoji}</span>`;
  });

  return container;
}

